function handleConversion(type, number) {
  switch (type) {
    case conversionTypes.CF:
      break;
    case conversionTypes.CK:
      break;
    case conversionTypes.FC:
      break;
    case conversionTypes.FK:
      break;
    case conversionTypes.KC:
      break;
    case conversionTypes.KF:
      break;

    default:
      return null;
      break;
  }
}

const conversionTypes = {
  CF: 1,
  CK: 2,
  FC: 3,
  FK: 4,
  KC: 5,
  KF: 6,
};
